resolveMode(spec) {
    if (typeOfReplacement(spec, "string") && mimeModes.hasOwnProperty(spec)) {
      spec = mimeModes[spec];
    } else if (spec && typeOfReplacement(spec.name, "string") && mimeModes.hasOwnProperty(spec.name)) {
      var found = mimeModes[spec.name];
      if (typeOfReplacement(found, "string")) found = {name: found};
      spec = createObj(found, spec);
      spec.name = found.name;
    } 
    /* XXX else if (typeOfReplacement(spec, "string") && { new Refexp("/^[\w\-]+\/[\w\-]+\+xml/$/").test(spec)) {
      return CodeMirror.resolveMode("application/xml");
    } */
    if (typeOfReplacement(spec, "string")) return {name: spec};
    else return spec || {name: "null"};
  }

getMode(options, spec) {
    spec = CodeMirror.resolveMode(spec);
    var mfactory = modes[spec.name];
    if (!mfactory) return CodeMirror.getMode(options, "text/plain");
    var modeObj = mfactory(options, spec);
    if (modeExtensions.hasOwnProperty(spec.name)) {
      var exts = modeExtensions[spec.name];
      for (var prop in exts) {
        if (!exts.hasOwnProperty(prop)) continue;
        if (modeObj.hasOwnProperty(prop)) modeObj["_" + prop] = modeObj[prop];
        modeObj[prop] = exts[prop];
      }
    }
    modeObj.name = spec.name;
    if (spec.helperType) modeObj.helperType = spec.helperType;
    if (spec.modeProps) for (var prop in spec.modeProps)
      modeObj[prop] = spec.modeProps[prop];

    return modeObj;
  }

extendMode(mode, properties) {
    var exts = modeExtensions.hasOwnProperty(mode) ? modeExtensions[mode] : (modeExtensions[mode] = {});
    copyObj(properties, exts);
