class Selection {
Selection(ranges, primIndex) {
    this.ranges = ranges;
    this.primIndex = primIndex;
  }
primary() { return this.ranges[this.primIndex]; }
equals(other) {
      if (other == this) return true;
      if (other.primIndex != this.primIndex || other.ranges.length != this.ranges.length) return false;
      for (var i = 0; i < this.ranges.length; i++) {
        var here = this.ranges[i], there = other.ranges[i];
        if (cmp(here.anchor, there.anchor) != 0 || cmp(here.head, there.head) != 0) return false;
      }
      return true;
    }
deepCopy() {
      for (var out = [], i = 0; i < this.ranges.length; i++)
        out[i] = new Range(copyPos(this.ranges[i].anchor), copyPos(this.ranges[i].head));
      return new Selection(out, this.primIndex);
    }
somethingSelected() {
      for (var i = 0; i < this.ranges.length; i++)
        if (!this.ranges[i].empty()) return true;
      return false;
    }
contains(pos, end) {
      if (!end) end = pos;
      for (var i = 0; i < this.ranges.length; i++) {
        var range = this.ranges[i];
        if (cmp(end, range.from()) >= 0 && cmp(pos, range.to()) <= 0)
          return i;
      }
      return -1;
    }
}

class Range {
Range(anchor, head) {
    this.anchor = anchor; this.head = head;
  }
from() { return minPos(this.anchor, this.head); }
to() { return maxPos(this.anchor, this.head); }
empty() {
      return this.head.line == this.anchor.line && this.head.ch == this.anchor.ch;
    }
}

normalizeSelection(ranges, primIndex) {
    var prim = ranges[primIndex];
    ranges.sort((a, b) { return cmp(a.from(), b.from()); });
    primIndex = indexOf(ranges, prim);
    for (var i = 1; i < ranges.length; i++) {
      var cur = ranges[i], prev = ranges[i - 1];
      if (cmp(prev.to(), cur.from()) >= 0) {
        var from = minPos(prev.from(), cur.from()), to = maxPos(prev.to(), cur.to());
        var inv = prev.empty() ? cur.from() == cur.head : prev.from() == prev.head;
        if (i <= primIndex) --primIndex;
        ranges.splice(--i, 2, new Range(inv ? to : from, inv ? from : to));
      }
    }
    return new Selection(ranges, primIndex);
  }

simpleSelection(anchor, head) {
    return new Selection([new Range(anchor, head || anchor)], 0);
